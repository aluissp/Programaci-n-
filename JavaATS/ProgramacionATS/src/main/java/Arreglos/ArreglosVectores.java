package Arreglos;

public class ArreglosVectores {

    public static void main(String[] args) {
        int[] numeros = new int[3];

        numeros[0] = 7;
        numeros[1] = 5;
        numeros[2] = 2;

        for (int i = 0; i < numeros.length; i++) {
            System.out.println(numeros[i]);
        }

    }
}


public class OpCombinados {

    public static void main(String[] args) {

        int num = 15;

        num += 5;//num=num+5;
        System.out.println(num);

        num -= 5;//num=num-5;
        System.out.println(num);

        num *= 5;//num=num*5;
        System.out.println(num);

        num /= 5;//num=num/5;
        System.out.println(num);

        num %= 5;//num=num%5;
        System.out.println(num);
    }
}

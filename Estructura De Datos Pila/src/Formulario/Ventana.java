    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (pila.getnElementos() > 0) {
                String reporte = "";

                Pila pilaAux = new Pila(pila.getnElementos());
                Notas n;
                while (!pila.Vacia()) {
                    n = (Notas) pila.Desapilar();
                    reporte += "\n----------------\nCédula: " + n.getCedula() + "   Código de materia: " + n.getCodMateria()
                            + "\nNota 1: " + n.getNota1() + "    Nota 2: "
                            + n.getNota2() + "\nNota supletorio: " + n.getNotaSupletorio();
                    pilaAux.Apilar(n);
                }

                while (!pilaAux.Vacia()) {
                    pila.Apilar(pilaAux.Desapilar());
                }
                this.txtCampoTexto.setText(reporte);
            }
        } catch (Exception e) {
            System.err.print("Error," + e);
            this.txtCampoTexto.setText("No se encuentra la pila creada");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btPruebaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPruebaActionPerformed
        try {
            Pila aprobados = Aprobados();
            String materia1 = this.txtMateria1.getText();
            String materia2 = this.txtMateria2.getText();
            String materia3 = this.txtMateria3.getText();
            Notas notas;
            int n1 = 0;
            int n2 = 0;
            int n3 = 0;

            while (!aprobados.Vacia()) {
                notas = (Notas) aprobados.Desapilar();
                if (notas.getCodMateria().equals(materia1)) {
                    n1++;
                } else if (notas.getCodMateria().equals(materia2)) {
                    n2++;
                } else if (notas.getCodMateria().equals(materia3)) {
                    n3++;
                }
            }

            if ((n1 >= n2) && (n1 >= n3)) {
                this.txtCampoTexto.setText("El número mayor de aprobados es " + n1 + " en la materia de: " + materia1);
            } else if ((n2 >= n1) && (n2 >= n3)) {
                this.txtCampoTexto.setText("El número mayor de aprobados es " + n2 + " en la materia de: " + materia2);
            } else if ((n3 >= n1) && (n3 >= n2)) {
                this.txtCampoTexto.setText("El número mayor de aprobados es " + n3 + " en la materia de: " + materia3);
            }
        } catch (Exception e) {
            this.txtCampoTexto.setText("Porfavor verifica si la pila esta creada");
            System.err.print("Error, " + e);
        }
    }//GEN-LAST:event_btPruebaActionPerformed

    private Pila Aprobados() {
        Pila pAprobados = new Pila(pila.getnElementos());
        Pila pAux = new Pila(pila.getnElementos());
        Notas notas;

        while (!pila.Vacia()) {
            notas = (Notas) pila.Cima();
            if ((notas.getNota1() + notas.getNota2()) >= 14
                    || (notas.getNota1() + notas.getNota2() + notas.getNotaSupletorio()) >= 21) {
                pAprobados.Apilar(notas);
            }
            pAux.Apilar(pila.Desapilar());
        }

        while (!pAux.Vacia()) {
            pila.Apilar(pAux.Desapilar());
        }

        return pAprobados;
    }


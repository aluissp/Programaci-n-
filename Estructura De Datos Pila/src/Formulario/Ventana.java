    private void btCrearPilaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCrearPilaActionPerformed
        try {
            int n = Integer.parseInt(this.txtTamañoPila.getText());

            if (n > 0) {
                pila = new Pila(n);
                this.txtCampoTexto.setText("Pila creada correctamente");
                vaciarCamposDeTextos();
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese un tamaño mayor a 0");
            }
        } catch (NumberFormatException e) {
            System.err.print("Error, " + e);
        }
    }//GEN-LAST:event_btCrearPilaActionPerformed

    private void btApilarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btApilarActionPerformed
        try {
            String cedula = this.txtCedula.getText();
            String codMateria = this.txtCodMateria.getText();
            double nota1 = Double.parseDouble(this.txtNota1.getText());
            double nota2 = Double.parseDouble(this.txtNota2.getText());
            double notaSupletorio = Double.parseDouble(this.txtNotaSupletorio.getText());

            notas = new Notas(cedula, codMateria, nota1, nota2, notaSupletorio);

            this.txtCampoTexto.setText(pila.Apilar(notas));

            vaciarCamposDeTextos();
        } catch (Exception e) {
            //this.txtCampoTexto.setText("No se encuentra creada la pila");
            System.err.print("Error, " + e);
        }
    }//GEN-LAST:event_btApilarActionPerformed

    private void btDesapilarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDesapilarActionPerformed
        try {
            notas = (Notas) pila.Desapilar();
            if (notas != null) {
                txtCampoTexto.setText("Nota\n" + "Cédula: " + notas.getCedula() + "\nCódigo de materia: " + notas.getCodMateria()
                        + "\nNota 1: " + notas.getNota1() + " Nota 2: " + notas.getNota2()
                        + "\nNota supletorio: " + notas.getNotaSupletorio() + "\nDesapilado correctamente");
            }
        } catch (NullPointerException e) {
            this.txtCampoTexto.setText("No se ecuentra creada la pila");
            System.err.print("Error, " + e);
        }
    }//GEN-LAST:event_btDesapilarActionPerformed

    private void btVaciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btVaciaActionPerformed
        try {
            if (pila.Vacia()) {
                this.txtCampoTexto.setText("La pila se ecuentra vacia");
            } else {
                this.txtCampoTexto.setText("La pila no se ecuentra vacia");
            }

        } catch (NullPointerException e) {
            this.txtCampoTexto.setText("No se ecuentra creada la pila");
        }
    }//GEN-LAST:event_btVaciaActionPerformed


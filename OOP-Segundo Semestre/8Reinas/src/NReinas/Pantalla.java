    @Override
    public void actionPerformed(ActionEvent ae) {
        centesimaSegundo++;

        if (centesimaSegundo == 100) {
            segundos++;
            centesimaSegundo = 0;
        } else if (segundos == 60) {
            minutos++;
            segundos = 0;
        } else if (minutos == 60) {
            horas++;
            minutos = 0;
        }

        actualizarTiempo();
    }

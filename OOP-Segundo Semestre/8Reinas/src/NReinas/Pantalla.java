    private void btJugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btJugarActionPerformed
        this.JComboSoluciones.setSelectedIndex(0);
        tiempo.start();
        tablero.activarTablero(true);
        this.btJugar.setEnabled(false);
        this.JComboSoluciones.setEnabled(true);
        horas = 0;
        minutos = 0;
        segundos = 0;
        centesimaSegundo = 0;

        actualizarTiempo();
    }//GEN-LAST:event_btJugarActionPerformed

    private void btTerminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTerminarActionPerformed
        if (tablero.comprobarSolucion()) {
            if (tiempo.isRunning()) {
                tiempo.stop();
                btJugar.setEnabled(true);
            }
            JOptionPane.showMessageDialog(null, "Felicidades has ganado la partida");
        } else {
            JOptionPane.showMessageDialog(null, "Has perdido, sigue intentando");
        }
    }//GEN-LAST:event_btTerminarActionPerformed

    private void btReiniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btReiniciarActionPerformed
        this.JComboSoluciones.setSelectedIndex(0);
        tablero.Reiniciar();
        horas = 0;
        minutos = 0;
        segundos = 0;
        centesimaSegundo = 0;

        actualizarTiempo();
    }//GEN-LAST:event_btReiniciarActionPerformed

    private void actualizarTiempo() {
        String tiempotxt = (horas < 10 ? "0" : "") + horas + ":" + (minutos < 10 ? "0" : "") + minutos + ":" + (segundos < 10 ? "0" : "")
                + segundos + ":" + (centesimaSegundo < 10 ? "0" : "") + centesimaSegundo;
        lbTiempo.setText(tiempotxt);
    }


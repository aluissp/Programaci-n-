    private void btReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btReporteActionPerformed
        try {
            if (!lista.listaVacia()) {
                this.txtCampoTexto.setText("Reporde de los productos:\n");
                Nodo aux = lista.getInicio();

                while (aux != null) {
                    this.txtCampoTexto.append("\n" + (Productos) aux.getInformacion());
                    aux = aux.getSiguiente();
                }
            } else {
                this.txtCampoTexto.setText("La lista de elementos esta vacia");
            }
        } catch (NullPointerException e) {
            this.txtCampoTexto.setText("Por favor cree la lista de elementos");
        }
    }//GEN-LAST:event_btReporteActionPerformed


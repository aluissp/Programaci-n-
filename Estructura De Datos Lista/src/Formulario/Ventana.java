    private void btIngresarPruebaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btIngresarPruebaActionPerformed
        try {
            String codigo = this.txtCodigo.getText();
            String nombre = this.txtNombre.getText();
            int cantidad = Integer.valueOf(this.txtCantidad.getText());
            double precio = Double.parseDouble(this.txtPrecio.getText());
            int minimo = Integer.parseInt(this.txtMinimo.getText());
            productos = new Productos(codigo, nombre, cantidad, precio, minimo);

            this.txtCampoTexto.setText(lista.ingresarPrueba(productos));
            LimpiarCajas();
        } catch (NumberFormatException e) {
            this.txtCampoTexto.setText("Por favor digite un número\nen los campos cantidad, precio, mínimo");
        }

    }//GEN-LAST:event_btIngresarPruebaActionPerformed

    private void btRetirarInicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRetirarInicioActionPerformed
        try {
            if (!lista.listaVacia()) {
                productos = (Productos) lista.retirarPrimerNodo();
                this.txtCampoTexto.setText("Se ha retirado el primer elemento: \n" + productos);
            } else {
                this.txtCampoTexto.setText("No hay elementos en la lista");
            }
        } catch (NullPointerException e) {
            this.txtCampoTexto.setText("Por favor cree la lista de elementos");
        }
    }//GEN-LAST:event_btRetirarInicioActionPerformed


    private void btExamenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExamenActionPerformed
        try {
            Pila pElementos = new Pila(cola.getnElementos());
            Pila pAux = new Pila(cola.getnElementos());

            while (!cola.Vacia()) {
                if (pElementos.Vacia()) {
                    pElementos.Apilar(cola.Retirar());

                } else if (((Productos) cola.Frente()).getPrecio() <= ((Productos) pElementos.Cima()).getPrecio()) {
                    pElementos.Apilar(cola.Retirar());

                } else if (((Productos) cola.Frente()).getPrecio() > ((Productos) pElementos.Cima()).getPrecio()) {

                    while (!pElementos.Vacia() && ((Productos) cola.Frente()).getPrecio() > ((Productos) pElementos.Cima()).getPrecio()) {
                        pAux.Apilar(pElementos.Desapilar());
                    }
                    pElementos.Apilar(cola.Retirar());

                    while (!pAux.Vacia()) {
                        pElementos.Apilar(pAux.Desapilar());
                    }
                }
            }

            while (!pElementos.Vacia()) {
                cola.Ingresar(pElementos.Desapilar());
            }
            
        } catch (NullPointerException e) {
            this.txtCampoTexto.setText("Por favor asegurese de haber creado la cola de elementos");
        }
    }//GEN-LAST:event_btExamenActionPerformed


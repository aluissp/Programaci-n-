    private void btReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btReporteActionPerformed
        try {
            if (!cola.Vacia()) {
                Cola aux = new Cola(cola.getnElementos());
                this.txtCampoTexto.setText("Reporte de los productos: ");
                while (!cola.Vacia()) {
                    productos = (Productos) cola.Frente();
                    this.txtCampoTexto.append("\n" + productos);

                    aux.Ingresar(cola.Retirar());
                }

                while (!aux.Vacia()) {
                    cola.Ingresar(aux.Retirar());
                }
            } else {
                this.txtCampoTexto.setText("No se encontro elementos en la cola");
            }
        } catch (NullPointerException e) {
            this.txtCampoTexto.setText("Asegurese de haber creado la cola de elementos");
        }
    }//GEN-LAST:event_btReporteActionPerformed


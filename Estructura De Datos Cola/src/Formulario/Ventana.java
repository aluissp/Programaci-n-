    private void btIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btIngresarActionPerformed
        try {
            String codigo = this.txtCodigo.getText();
            String nombre = this.txtNombre.getText();
            int cantidad = Integer.valueOf(this.txtCantidad.getText());
            double precio = Double.parseDouble(this.txtPrecio.getText());
            int minimo = Integer.parseInt(this.txtMinimo.getText());
            productos = new Productos(codigo, nombre, cantidad, precio, minimo);

            if (!cola.Llena()) {
                LimpiarCajas();
            }
            this.txtCampoTexto.setText(cola.Ingresar(productos));
        } catch (NumberFormatException e) {
            this.txtCampoTexto.setText("Por favor digite un número\nen los campos cantidad, precio, mínimo");
        } catch (NullPointerException e) {
            this.txtCampoTexto.setText("Por favor cree la cola para ingresar elementos");
        }
    }//GEN-LAST:event_btIngresarActionPerformed


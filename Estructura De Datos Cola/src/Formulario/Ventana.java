    private void btVenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btVenderActionPerformed
        try {
            String codigo = this.txtCodigo.getText();
            int cantidad = Integer.valueOf(this.txtCantidad.getText());

            if (!cola.Vacia()) {
                Cola aux = new Cola(cola.getnElementos());

                while (!cola.Vacia()) {
                    productos = (Productos) cola.Frente();
                    if (productos.getCodigo().equals(codigo)) {
                        if ((productos.getCantidad() - cantidad) > 0) {
                            if (JOptionPane.showConfirmDialog(null, "Esta seguro de vender\n" + cantidad + " " + productos.getNombre() + "s") == 0) {
                                productos.setCantidad(productos.getCantidad() - cantidad);
                                this.txtCampoTexto.setText("Se ha vendido: " + cantidad + " " + productos.getNombre() + "s"
                                        + "\n" + productos);
                            } else {
                                this.txtCampoTexto.setText("Ha cancelado la venta del producto");
                            }
                        } else {
                            this.txtCampoTexto.setText("Ya no se dispone de producto en el inventario"
                                    + "\nProducto disponible: " + productos.getCantidad());
                        }
                        while (!cola.Vacia()) {
                            aux.Ingresar(cola.Retirar());
                        }
                    } else {
                        this.txtCampoTexto.setText("No se econtro ningun producto con el código digitado");
                    }
                    aux.Ingresar(cola.Retirar());
                }

                while (!aux.Vacia()) {
                    cola.Ingresar(aux.Retirar());
                }
            } else {
                this.txtCampoTexto.setText("No se encontro elementos en la cola");
            }

        } catch (NumberFormatException e) {
            this.txtCampoTexto.setText("Por favor digite un número en la cantidad");
        } catch (NullPointerException e) {
            this.txtCampoTexto.setText("Por favor cree la cola para ingresar elementos");
        }
    }//GEN-LAST:event_btVenderActionPerformed

